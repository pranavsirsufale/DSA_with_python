# Inheritance Example
class Vehicle:
    def __init__(self, make, model, year):
        self.make = make
        self.model = model
        self.year = year

class Car(Vehicle):
    def __init__(self, make, model, year, doors):
        super().__init__(make, model, year)
        self.doors = doors

car = Car("Toyota", "Camry", 2022, 4)
print(car.make, car.model, car.year, car.doors)
